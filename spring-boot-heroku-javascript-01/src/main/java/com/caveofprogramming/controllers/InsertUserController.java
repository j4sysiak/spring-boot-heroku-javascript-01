	@Autowired
	private InterestService interestService;
 
	@RequestMapping(value = "/insertuser", method={RequestMethod.POST})
    ModelAndView insertuser(ModelAndView modelAndView, BindingResult result) {
		  
		modelAndView.setViewName("app.insertdata");

		if (!result.hasErrors()) {

			Random random = new Random();

			List<String> names = loadNames(); 
			List<String> interests = loadInterests();

			for (int numUsers = 0; numUsers < NUM_USER; numUsers++) {

				String firstname = names.get(random.nextInt(names.size()));
				String surname = names.get(random.nextInt(names.size()));

				String email = firstname.toLowerCase() + surname.toLowerCase() + "@example.com";

				if (siteUserService.get(email) != null) {
					continue;
				}
				
				if (siteUserService.get(firstname) != null  && siteUserService.get(surname) != null) {
					continue;
				}
			 

				String password = "pass" + firstname.toLowerCase();
				password = password.substring(0,
						Math.min(15, password.length()));

				SiteUser user = new SiteUser(email, password, firstname,
						surname);
				user.setEnabled(random.nextInt(5) != 0); // co piaty bedzie disable

				System.out.println(user);

				siteUserService.register(user);

				Profile profile = new Profile(user);

				int numberInterests = random.nextInt(7);

				Set<Interest> userInterests = new HashSet<Interest>();

				for (int i = 0; i < numberInterests; i++) {

					String interestText = interests.get(random
							.nextInt(interests.size()));

					Interest interest = interestService
							.createIfNotExists(interestText);
					userInterests.add(interest);
				}

				profile.setInterests(userInterests);
				profileService.save(profile);
			}

		}

		 return modelAndView;
	}
	
	

